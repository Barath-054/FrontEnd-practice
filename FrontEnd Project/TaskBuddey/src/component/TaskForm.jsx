import React, { useState } from 'react'

export default function TaskForm(addTask) {
let [task, setTask] = useState("");
let [priority, setPriority] = useState("Medium");
let [category, setCategory] = useState("General");

const handlesubmit = (e) => {
  e.preventDefault();
  if (task.trim()) {
  addTask({text:task, priority, category, completed:false})
    setTask("");
    setPriority("Medium");
    setCategory("General");
  }
}

  return (
    <div>
      <form className='task-form' onSubmit={handlesubmit}>
        <div id="inp">
          <input type ="text"  value = {task} placeholder='Enter the task' onChange= {(e) => setTask(e.target.value)} />
          <span>
            <button type="submit">Add task</button>
          </span>
        </div>
          <div id= "btns">
            <select value = {priority} onChange={(e) => setPriority(e.target.value)}>
              <option value="High">High</option>
              <option value="Medium">Medium</option>
              <option value="Low">Low</option>
            </select>
            <select vlaue = {category} onChange={(e) => setCategory(e.target.value)}>
              <option value="General">General</option>
              <option value="Work">Work</option>
              <option value="Persional">Persional</option>
            </select>
          </div>
        
      </form>
    </div>
  )
}
